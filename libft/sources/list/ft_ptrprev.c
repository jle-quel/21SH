#include "../../includes/libft.h"

/*
*************** PUBLIC *********************************************************
*/

void			ft_ptrprev(t_list **node, size_t stop)
{
	size_t		index;

	index = 0;
	if (*node)
	{
		while ((*node)->prev)
		{
			if (index == stop)
				break ;
			*node = (*node)->prev;
			index++;
		}
	}
}
